    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        // Get the product name to search for
  String productName = jTextField4.getText();

// Read the contents of the "Products.txt" file
boolean productFound = false;
try (Scanner scanner = new Scanner(new File("Products.txt"))) {
    // Iterate through each line in the file
    while (scanner.hasNextLine()) {
        String line = scanner.nextLine();
        // Split the line into parts using "/"
        String[] parts = line.split("/");

        // Check if the product name matches the searched name
        if (parts.length > 0 && parts[0].equals(productName)) {
            // Display the model, price, and quantity in the corresponding text fields
            jTextField1.setText(parts.length > 1 ? parts[2] : "");
            jTextField2.setText(parts.length > 4 ? parts[4] : "");
            jTextField3.setText(parts.length > 3 ? parts[3] : "");
            // Set the flag to indicate that the product is found
            productFound = true;
            // Break out of the loop since the product is found
            break;
        }
    }
} catch (FileNotFoundException e) {
    // Handle the case where the file is not found
    JOptionPane.showMessageDialog(null, "Products.txt file not found.");
     
    
}

// Check if the product was not found
if (!productFound) {
    JOptionPane.showMessageDialog(null, "Product not found.");
    jTextField4.setText("");
}

    }//GEN-LAST:event_jButton3MouseClicked

    private void jUpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jUpdateButtonActionPerformed
        // TODO add your handling code here:
          String updatedModel = jTextField1.getText();
    String updatedPrice = jTextField2.getText();
    String updatedQuantity = jTextField3.getText();
    String productNameToUpdate = jTextField4.getText();

    // Read the contents of the "Products.txt" file
    List<String> lines = new ArrayList<>();
    try (Scanner scanner = new Scanner(new File("Products.txt"))) {
        // Iterate through each line in the file
        while (scanner.hasNextLine()) {
            String line = scanner.nextLine();
            // Split the line into parts using "/"
            String[] parts = line.split("/");

            // Check if the product name matches the product to update
            if (parts.length > 0 && parts[0].equals(productNameToUpdate)) {
                // Update the model, price, and quantity
                if (!updatedModel.isEmpty()) {
                    parts[1] = updatedModel;
                }
                if (!updatedPrice.isEmpty()) {
                    parts[4] = updatedPrice;
                }
                if (!updatedQuantity.isEmpty()) {
                    parts[3] = updatedQuantity;
                }
                // Reconstruct the line with the updated information
                line = String.join("/", parts);
            }
            // Add the line to the list
            lines.add(line);
        }
    } catch (FileNotFoundException e) {
        // Handle the case where the file is not found
        JOptionPane.showMessageDialog(null, "Products.txt file not found.");
        return; // Exit the method if the file is not found
    }

    // Write the updated content back to the "Products.txt" file
    try (PrintWriter writer = new PrintWriter(new FileWriter("Products.txt"))) {
        for (String line : lines) {
            writer.println(line);
        }
        JOptionPane.showMessageDialog(null, "Product updated successfully.");
    } catch (IOException e) {
        // Handle the case where there is an error writing to the file
        JOptionPane.showMessageDialog(null, "Error updating product.");
    }
    // Clear the displayed information
    jTextField1.setText("");
    jTextField2.setText("");
    jTextField3.setText("");
    jTextField4.setText("");
    }//GEN-LAST:event_jUpdateButtonActionPerformed


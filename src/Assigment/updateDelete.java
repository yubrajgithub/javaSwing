    private void jDeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDeleteButtonActionPerformed
        // TODO add your handling code here:
        // Get the product name to delete
    String productNameToDelete = jTextField4.getText();

    // Read the contents of the "Products.txt" file
    List<String> lines = new ArrayList<String>();

    try (Scanner scanner = new Scanner(new File("Products.txt"))) {
        // Iterate through each line in the file
        while (scanner.hasNextLine()) {
            String line = scanner.nextLine();
            // Split the line into parts using "/"
            String[] parts = line.split("/");

            // Check if the product name matches the product to delete
            if (parts.length > 0 && !parts[0].equals(productNameToDelete)) {
                // Add the line to the list if it's not the product to delete
                lines.add(line);
            }
        }
    } catch (FileNotFoundException e) {
        // Handle the case where the file is not found
        JOptionPane.showMessageDialog(null, "Products.txt file not found.");
        return; // Exit the method if the file is not found
    }

    // Write the updated content back to the "Products.txt" file
    try (PrintWriter writer = new PrintWriter(new FileWriter("Products.txt"))) {
        for (String line : lines) {
            writer.println(line);
        }
        JOptionPane.showMessageDialog(null, "Product deleted successfully.");
    } catch (IOException e) {
        // Handle the case where there is an error writing to the file
        JOptionPane.showMessageDialog(null, "Error deleting product.");
    }

    // Clear the displayed information
    jTextField1.setText("");
    jTextField2.setText("");
    jTextField3.setText("");
    jTextField4.setText("");
    }//GEN-LAST:event_jDeleteButtonActionPerformed

